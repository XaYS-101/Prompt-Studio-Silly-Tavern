function stLanguage() {
    try {
        const lang = localStorage.getItem('language') || navigator.language || 'en';
        return lang.toLowerCase().startsWith('ru') ? 'ru' : 'en';
    } catch {
        return 'en';
    }
}

export function getLang() {
    const setting = extension_settings[MODULE]?.language ?? 'ru';
    if (setting === 'auto') return stLanguage();
    return setting === 'en' ? 'en' : 'ru';
}

/** t('key', { n: 3 }) — ${n} interpolation, lang → en → key fallback. */
export function t(key, vars = null) {
    const lang = getLang();
    let text = I18N[lang]?.[key] ?? I18N.en[key] ?? key;
    if (vars) {
        for (const [name, value] of Object.entries(vars)) {
            text = text.replaceAll('${' + name + '}', String(value));
        }
    }
    return text;
}

/**
 * Walk [data-ps-i18n] under root. Spec: "key" sets textContent;
 * "[attr]key" sets an attribute; multiple specs separated by ";".
 */
export function localize(root) {
    const nodes = root.querySelectorAll('[data-ps-i18n]');
    for (const node of nodes) {
        const spec = node.getAttribute('data-ps-i18n');
        if (!spec) continue;
        for (const part of spec.split(';')) {
            const trimmed = part.trim();
            if (!trimmed) continue;
            const match = trimmed.match(/^\[(.+?)\](.+)$/);
            if (match) node.setAttribute(match[1], t(match[2]));
            else node.textContent = t(trimmed);
        }
    }
    if (root.matches?.('[data-ps-i18n]')) {
        const spec = root.getAttribute('data-ps-i18n');
        const match = spec?.match(/^\[(.+?)\](.+)$/);
        if (spec && !match) root.textContent = t(spec);
        else if (match) root.setAttribute(match[1], t(match[2]));
    }
}
